
public class MathInJava {
	public static void main(String[]args){
		int result = Math.max(50, 100);
		int result2 = Math.min(50, 100);
		double result3 = Math.random()*10;
		double result4 = Math.ceil(10);
		double result5 = Math.cos(25);
		double result6 = Math.log(10);
		double result7 = Math.sin(25);
		double result8 = Math.tan(25);
		double result9 = Math.pow(10, 15);
		double result10 = Math.floor(10);
		
		System.out.println("Maximum number is "+ result);
		System.out.println("Minimum number is "+ result2);
		System.out.println("Random number is "+ result3);
		System.out.println("Ceil number is "+ result4);
		System.out.println("Cos number is "+ result5);
		System.out.println("Log number is "+ result6);
		System.out.println("Sin number is "+ result7);
		System.out.println("Tan number is "+ result8);
		System.out.println("Pow number is "+ result9);
		System.out.println("Floor number is "+ result10);
		
		
		
	}

}
